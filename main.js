// INCREMENT & DECREMENT OPERATORS
//1.INCREMENT:(++)
//It is same like adition but it only add 1 in a variable everytime
//There are two types,
//1.Pre-Increment:(++a)
// Pre-Increment is to increase the numerical count of the variable by a value of 1.
/*
let a = 10;

console.log(++a);   //here answer will be 11.
console.log(++a);   //here answer will be 12.
*/
//2.Post-Increment:(a++)
//In Post-Decrement,the value of the variable is first used in an expression and then increase by a value of 1.
/*
let b = 20;

console.log(b++);   //also here answer will be 20.
console.log(b++);   //Now, answer will be 21.
*/
//2.DECREMENT:(--)
//It is same like adition but it only sub 1 in a variable everytime
//There are two types,
//1.Pre-Decrement:(--c)
// Pre-Increment is to decrease the numerical count of the variable by a value of 1.
/*
let c = 30;

console.log(--c);   //here answer will be 29.
console.log(--c);   //here answer will be 28.
*/
//2.Post-Decrement:(d--)
//In Post-Decrement,the value of the variable is first used in an expression and then increase by a value of 1.
/*
let d = 40;

console.log(d--);   //also here answer will be 40.
console.log(d--);   //Now, answer will be 39.
*/
